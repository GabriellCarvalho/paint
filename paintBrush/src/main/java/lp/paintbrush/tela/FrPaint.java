    private void jpTelaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jpTelaMousePressed
        if (null != tipoFigura)
            switch (tipoFigura) {
                case tpPonto:
                    p.setX(evt.getX());
                    p.setY(evt.getY());
                    p.setCor(jpCor.getBackground());
                    p.desenha(jpTela.getGraphics());
                    break;
                case tpReta:
                    re.setX(evt.getX());
                    re.setY(evt.getY());
                    break;
                case tpRetangulo:
                    r.setX(evt.getX());
                    r.setY(evt.getY());
                    break;
                case tpBorracha:
                    b.setX(evt.getX());
                    b.setY(evt.getY());
                    break;
                case tpCirculo:
                    c.setX(evt.getX());
                    c.setY(evt.getY());
                    break;
                case tpCilindro:
                    cl.setX(evt.getX());
                    cl.setY(evt.getY());
                    break;
                case tpPiramide:
                    pr.setX(evt.getX());
                    pr.setY(evt.getY());
                    break;
                case tpSpray:
                    sp.setCor(jpCor.getBackground());
                    sp.setX(evt.getX());
                    sp.setY(evt.getY());
                    sp.desenha(jpTela.getGraphics());
                    break;
                case tpPoligono:
                    po.setX(evt.getX());
                    po.setY(evt.getY());
                    po.setCor(jpCor.getBackground());
                    po.setCorInterna(jpCorInterna.getBackground());
                    po.desenha(jpTela.getGraphics());
                    break;
                default:
                    break;
            }
    }//GEN-LAST:event_jpTelaMousePressed

    private void jpTelaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jpTelaMouseReleased
        if (null != tipoFigura)
            switch (tipoFigura) {
                case tpReta:
                    re.setX1(evt.getX());
                    re.setY1(evt.getY());
                    re.setCor(jpCor.getBackground());
                    re.setShowArea(cbArea.getState());
                    re.desenha(jpTela.getGraphics());
                    break;
                case tpRetangulo:
                    r.setBase(evt.getX() - r.getX());
                    r.setLargura(evt.getY() - r.getY());
                    r.setCor(jpCor.getBackground());
                    r.setCorInterna(jpCorInterna.getBackground());
                    r.setShowArea(cbArea.getState());
                    r.desenha(jpTela.getGraphics());
                    break;
                case tpBorracha:
                    b.setCor(jpTela.getBackground());
                    b.desenha(jpTela.getGraphics());
                    break;
                case tpCirculo:
                    c.setRaio((evt.getX() - c.getX()) / 2);
                    c.setCor(jpCor.getBackground());
                    c.setCorInterna(jpCorInterna.getBackground());
                    c.setShowArea(cbArea.getState());
                    c.desenha(jpTela.getGraphics());
                    break;
                case tpCilindro:
                    cl.setRaio((evt.getX() - cl.getX()) / 2);
                    cl.setAltura(evt.getX() - cl.getX());
                    cl.setCor(jpCor.getBackground());
                    cl.setCorInterna(jpCorInterna.getBackground());
                    cl.setShowArea(cbArea.getState());
                    cl.desenha(jpTela.getGraphics());
                    break;
                case tpPiramide:
                    pr.setBase(evt.getX() - pr.getX());
                    pr.setLargura(evt.getY() - pr.getY());
                    pr.setAltura(2 * pr.getLargura());
                    pr.setCor(jpCor.getBackground());
                    pr.setCorInterna(jpCorInterna.getBackground());
                    pr.setShowArea(cbArea.getState());
                    pr.desenha(jpTela.getGraphics());
                    break;
                default:
                    break;
            }
    }//GEN-LAST:event_jpTelaMouseReleased

